// زمانی ڕاستەوخۆ لە localStorage یان browser دیاری بکە
function getCurrentLanguage() {
  let savedLang = localStorage.getItem('exinflix_language');
  if (savedLang && translations[savedLang]) {
    return savedLang;
  }
  // زمانی browser
  let browserLang = navigator.language.split('-')[0];
  if (browserLang === 'ckb' || browserLang === 'ku') return 'ckb';
  if (browserLang === 'ar') return 'ar';
  if (browserLang === 'en') return 'en';
  if (browserLang === 'tr') return 'tr';
  if (browserLang === 'fa') return 'fa';
  if (browserLang === 'ru') return 'ru';
  if (browserLang === 'fr') return 'fr';
  if (browserLang === 'es') return 'es';
  if (browserLang === 'pt') return 'pt';
  if (browserLang === 'de') return 'de';
  if (browserLang === 'sv') return 'sv';
  if (browserLang === 'it') return 'it';
  if (browserLang === 'zh') return 'zh';
  if (browserLang === 'ko') return 'ko';
  return 'ckb'; // default
}

// فەرمانی تەرجەمەکردن
function t(key) {
  const lang = getCurrentLanguage();
  return translations[lang]?.[key] || translations['ckb'][key] || key;
}

// گۆڕینی زمانی پەڕە
function setLanguage(langCode) {
  if (translations[langCode]) {
    localStorage.setItem('exinflix_language', langCode);
    location.reload(); // نوێکردنەوەی پەڕە بۆ تەرجەمەکردن
  }
}
